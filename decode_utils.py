import numpy as np


def decode_qa(start_pos, end_pos):
    res = []
    for start_p, end_p in zip(start_pos, end_pos):
        if start_p == 0 and end_p == 0:
            continue
        # 这里按照最近的进行解码
        res.append((start_p, end_p))
    return res


def calculate_metric(gt, predict):
    """
    计算 tp fp fn
    """
    tp, fp, fn = 0, 0, 0
    for entity_predict in predict:
        flag = 0
        for entity_gt in gt:
            if entity_predict[0] == entity_gt[0] and entity_predict[1] == entity_gt[1]:
                flag = 1
                tp += 1
                break
        if flag == 0:
            fp += 1

    fn = len(gt) - tp

    return tp, fp, fn


def get_p_r_f(tp, fp, fn):
    p = tp / (tp + fp) if tp + fp != 0 else 0
    r = tp / (tp + fn) if tp + fn != 0 else 0
    f1 = 2 * p * r / (p + r) if p + r != 0 else 0
    return p, r, f1


